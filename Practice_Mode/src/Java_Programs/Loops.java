package Java_Programs;


public class Loops {
	
	public static void main(String[] args) {
		
		for (int i = 1; i <=3; i++) {
			
			for (int j = 1; j <=3; j++) {
				System.out.print(j);
				
			}
			
			System.out.println();
			
		}
		for (int i = 5; i >1; i--) {
			System.out.println(i);
			
		}
		for (int i = 550; i <=555; i++) {
			System.out.println(8);
		}
		for (int j = 0; j <10; j++) {
			
		if (j==5) {
			//System.out.println(i);
			break;
			
		}
		System.out.println(j);
	}
	
	}
	
	

}
